package week1.day04_concatenation;

public class PrimitiveTypeCastings {
    public static void main(String[] args) {

        byte a = 15;
        short b = (short)a;

        System.out.println(b);

        int c = a;

        System.out.println(c);

        long d = 3000L;

        float f = d;

        System.out.println(f);

        int x = 100;
        byte y = (byte)x;

        double u = 4000.2;
        int k = (int)u;
        System.out.println(k);



    }
}
